void Mostrar_imagen(String ruta_imagen){
    try{
    if(Main.fin==null){
      imagen.setText("pila vacia");
      imagen.setIcon(null);
    }else
    {    
    ImageIcon foto;
    foto = new ImageIcon(ruta_imagen);
    Icon icono;
    icono = new ImageIcon(foto.getImage()
            .getScaledInstance(imagen.getWidth(),imagen.getHeight()
                    ,Image.SCALE_DEFAULT));//adapataci칩n al tama침o de la etiqueta
        imagen.setIcon(icono);
    }
    }catch(Exception ex){
            JOptionPane.showMessageDialog(null,
   "Error al abrir el archivo","Advertencia",
   JOptionPane.WARNING_MESSAGE);
        }

    }

void rIzquierda() {
    if(Main.fin == null) {
        imagen.setText("pila vacia");
        imagen.setIcon(null);
    } else {
        auxiliar = auxiliar.getAnterior();
        JCodigo.setText(auxiliar.getCodigo());
        Mostrar_imagen(auxiliar.getRutaImagen());
    }
}

void rDerecha() {
    if(Main.fin == null) {
        imagen.setText("pila vacia");
        imagen.setIcon(null);
    } else {
        auxiliar = auxiliar.getSiguiente();
        JCodigo.setText(auxiliar.getCodigo());
        Mostrar_imagen(auxiliar.getRutaImagen());
    }
}

