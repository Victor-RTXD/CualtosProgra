package proyecto11;
import java.io.*;

public class Anuario {
    String nombre;
    String generacion;
    String direccionFoto;
    String fechaCumple;

    public Anuario(String nombre, String generacion, String direccionFoto, String fechaCumple) {
        this.nombre = nombre;
        this.generacion = generacion;
        this.direccionFoto = direccionFoto;
        this.fechaCumple = fechaCumple;
    }
}